# Write a Python program to separate keys and values from a dictionary using keys() and values() methods. 

my_dict = {
    "first name" : "shrey",
    "surname" : "patel",
    "subject" : "python",
    "marks" : 45,
    "city" : "ahmedabad",
    "state" : "gujarat"

}

keys = my_dict.keys()
values = my_dict.values()

print(keys)
print(values)
